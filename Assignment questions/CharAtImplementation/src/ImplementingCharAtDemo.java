
public class ImplementingCharAtDemo {

public static void main(String args[]){
		
		String inputString = "Metacube Employees";
		System.out.println("Character at 6th index : "+inputString.charAt(6));
		char characterArray[] = inputString.toCharArray();
		System.out.println("Character at 6th index : "+characterArray[6]);
		
	}
}
